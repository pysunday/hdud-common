const pres = [];
export default function random(ex = []) {
  const keys = Object.keys(cowsays).filter(it => !ex.includes(it) && !pres.includes(it))
  const key = keys[Math.floor(Math.random() * keys.length)];
  pres.push(key);
  if (pres.length > 10) pres.shift();
  return { name: key, value: cowsays[key].replace(/^\r?\n/, '').replace(/\r?\n$/, '') };
}

